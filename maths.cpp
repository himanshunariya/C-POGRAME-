#include<iostream>
 
#include<stdlib.h>

using namespace std;
int main()
{
    cout<<"value of floor is: "<<floor(5.9)<<endl;
    cout<<"value of floor is: "<<floor(-15.9)<<endl;
    cout<<"value of ceil  is: "<<ceil(5.2)<<endl;
    cout<<"value of ceil  is: "<<ceil(-15.6)<<endl;
    cout<<"value of round is: "<<round(5.2)<<endl;
    cout<<"value of round is: "<<round(-15.6)<<endl;
    cout<<"value of sqrt  is: "<<sqrt(16)<<endl;
    cout<<"value of sqrt  is: "<<sqrt(5)<<endl;
    cout<<"value of pow   is: "<<pow(2,8)<<endl;
    cout<<"value of pow   is: "<<pow(5,3)<<endl;
    cout<<"value of abs   is: "<<abs(16)<<endl;
    cout<<"value of abs   is: "<<abs(-5)<<endl;
    cout<<"value of rand  is: "<<rand()%5<<endl;
    cout<<"value of rand  is: "<<rand()%100<<endl;
    cout<<"value of rand  is: "<<rand()%100<<endl;
}